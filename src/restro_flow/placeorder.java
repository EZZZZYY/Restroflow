    public void validateField() {
        String customerName = txtCustomerName.getText();
        String customerPhoneNumber = txtPhoneNumber.getText();
        String customerAddress = txtAddress.getText();
        if (!customerAddress.equals("") && customerPhoneNumber.matches(phoneNumberPattern) && customerPhoneNumber.length() == 10 && grandTotal > 0) {
            btnGenerateBill.setEnabled(true);
        } else {
            btnGenerateBill.setEnabled(false);
        }
    }

