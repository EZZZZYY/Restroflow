    private void btnGenerateBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateBillActionPerformed
        String customerName = txtCustomerName.getText();
        String customerPhoneNumber = txtPhoneNumber.getText();
        String customerAddress = txtAddress.getText();
        SimpleDateFormat dFormat = new SimpleDateFormat("dd-MM-yyyy");
        Date date = new Date();
        String todaydate = dFormat.format(date);
        String total = String.valueOf(grandTotal);
        String createdBy = userEmail;
        ORDERBILL bill = new ORDERBILL();
        bill.setId(billId);
        bill.setName(customerName);
        bill.setMobileNumber(customerPhoneNumber);
        bill.setAddress(customerAddress);
        bill.setDate((todaydate));
        bill.setTotal(total);
        bill.setCreatedBy(createdBy);
        orderbilldata.save(bill);

        //now its a bill time 
        String path = "D:\\BILL FOR RESTROFLOW";
        com.itextpdf.text.Document doc = new com.itextpdf.text.Document();
        try {
            PdfWriter.getInstance(doc, new FileOutputStream(path + billId + ".pdf"));
            doc.open();
            Paragraph cafeName = new Paragraph("								DEMO CAFE FOR RESTRO-FLOW\n");
            doc.add(cafeName);
            Paragraph gapLine = new Paragraph("																						\n");
            doc.add(gapLine);
            Paragraph custdetails = new Paragraph("\tBILL ID: " + billId + "\nCUSTOMER NAME: " + customerName + " \nTOTAL BILL PAID: " + grandTotal);
            doc.add(custdetails);
            Paragraph gapLine1 = new Paragraph("																						\n");
            doc.add(gapLine1);
            PdfPTable tb1 = new PdfPTable(4);
            tb1.addCell("NAME");
            tb1.addCell("PRICE");
            tb1.addCell("QUANTITY");
            tb1.addCell("TOTAL");
            for (int i = 0; i < jTable2.getRowCount(); i++) {
                String n = jTable2.getValueAt(i, 0).toString();
                String p = jTable2.getValueAt(i, 1).toString();
                String q = jTable2.getValueAt(i, 2).toString();
                String t = jTable2.getValueAt(i, 3).toString();
                tb1.addCell(n);
                tb1.addCell(p);
                tb1.addCell(q);
                tb1.addCell(t);
                
            }
            doc.add(tb1);
            doc.add(gapLine);
            Paragraph farewell = new Paragraph("								THANKS FOR THE VISIT\n");
            doc.add(farewell);

//            g open the bill
            openBill.openById(String.valueOf(billId));
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
        doc.close();
        setVisible(false);
        new placeorder(createdBy).setVisible(true);
        

    }//GEN-LAST:event_btnGenerateBillActionPerformed

    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        
        int index = jTable2.getSelectedRow();
        int a = JOptionPane.showConfirmDialog(null, "DO YOU WANT TO REMOVE THE PRODUCT?", "REMOVE", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            TableModel model = jTable2.getModel();
            String total = model.getValueAt(index, 3).toString();
            grandTotal = grandTotal - Integer.parseInt(total);
            lblGrandTotal.setText(String.valueOf(grandTotal));
            ((DefaultTableModel) jTable2.getModel()).removeRow(index);
        }
        

    }//GEN-LAST:event_jTable2MouseClicked

